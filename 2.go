//простейшие операции
package main

import "fmt"

func main() {
	fmt.Println("go" + "lang")
	fmt.Println("10+2=", 10+2)
	fmt.Println("10*2=", 10*2)
	fmt.Println("10.0/3.0=", 10.0/3.0)
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
